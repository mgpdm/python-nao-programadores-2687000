# Crie duas variáveis do tipo numérica, uma sinalizando a fase atual do curso 
# e outra o nível final
nivel_atual = 1
nivel_final = 6

# Crie um while loop que imprima na tela o nível atual
while nivel_atual <= nivel_final:
  print('O seu nível atual é ',nivel_atual,".")
  nivel_atual += 1
else:
  print('Parabéns, você concluiu o curso.')

